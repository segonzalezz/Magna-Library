    private void txtNom_cateKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNom_cateKeyTyped
        if(!(Character.isLetter(evt.getKeyChar()))){
            evt.consume();
        }
    }//GEN-LAST:event_txtNom_cateKeyTyped

    private void txtId_CateKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtId_CateKeyTyped
        int key = evt.getKeyChar();
        boolean numero = key >= 48 && key <= 57;
        if (!numero) {
            evt.consume();
        }
    }//GEN-LAST:event_txtId_CateKeyTyped

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String id_categoria = txtId_Cate.getText();
        String nom_categoria = txtNom_cate.getText();
        Categoria categoria = new Categoria (id_categoria, nom_categoria);
        try {
            if (controlador.registrarCategoria(categoria)) {
                JOptionPane.showMessageDialog(null, "Se registr贸 la categoria correctamente con c贸digo: " + id_categoria);
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(null, "No se registr贸 la categoria con c贸digo: " + id_categoria);
            }
        } catch (CategoriaDuplicadaException e) {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       String id_categoria = txtId_Cate.getText();
       Categoria categoria = controlador.obtenerCategoria(id_categoria);
       if(categoria != null){
           txtNom_cate.setText(categoria.getNom_categoria());
       }else{
           JOptionPane.showMessageDialog(null,"No se encontro ninguna categoria con el codigo: " + id_categoria);
       }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void limpiarCampos(){
        txtId_Cate.setText("");
        txtNom_cate.setText("");
    }
