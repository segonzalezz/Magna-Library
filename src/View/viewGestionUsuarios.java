    public viewGestionUsuarios() {
        initComponents();
        controlador = new ControladorRegistroUsuario();
        setLocationRelativeTo(this);
        try{
            DefaultTableModel modelo = new DefaultTableModel();
            tableU.setModel(modelo);
            PreparedStatement ps = null;
            ResultSet rs = null;
            Singleton conexion = new Singleton();
            Connection con = (Connection) conexion.conectar();
            String selectQuery = "SELECT * FROM usuario";
            ps = con.prepareStatement(selectQuery);
            rs = ps.executeQuery();
            ResultSetMetaData rsMd = (ResultSetMetaData) rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            modelo.addColumn("Nombre");
            modelo.addColumn("Edad");
            modelo.addColumn("Cedula"); 
            modelo.addColumn("Direccion");
            modelo.addColumn("User");
            modelo.addColumn("Password");
            modelo.addColumn("Rol");
            int [] anchos = {120, 50, 100, 100, 100, 100, 100, 130};
            for (int i = 0; i < tableU.getColumnCount(); i++) {
                tableU.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }
            while(rs.next()){
                Object[] filas = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modelo.addRow(filas);
            }  
        } catch (SQLException ex){
            System.err.println(ex.toString());
        }
    }

