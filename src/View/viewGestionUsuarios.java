    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tableU.getSelectedRow();
        if (filaSeleccionada != -1) {
            String cedulaAEliminar = (String) tableU.getValueAt(filaSeleccionada, 2);
            if (controlador.eliminarU(cedulaAEliminar)) {
                DefaultTableModel model = (DefaultTableModel) tableU.getModel();
                model.removeRow(filaSeleccionada);
                JOptionPane.showMessageDialog(null, "Usuario eliminado con cédula: " + cedulaAEliminar);
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró usuario con cédula: " + cedulaAEliminar);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecciona un usuario para eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

