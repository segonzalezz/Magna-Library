    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String id_prestamo = txtId_prestamo.getText();
        Date fechaSeleccionadaUno = jDateChooser1.getDate();
        SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");        
        String dateS_prestamo = formatoFecha.format(fechaSeleccionadaUno);
        Date fechaSeleccionadaDos = jDateChooser2.getDate();
        String dateF_prestamo = formatoFecha.format(fechaSeleccionadaDos);
        Usuario usuario = SesionMain.getUsuarioAutenticado();
        String cod_user = usuario.getUser_u();
        String selectedValue = cboLibros.getSelectedItem().toString();
        String[] partes = selectedValue.split(" - ");
        String cod_libro = partes[0];
        Prestamo prestamo = new Prestamo(id_prestamo, dateS_prestamo, dateF_prestamo, cod_user, cod_libro);
        try {
            if (controladorDos.registrarPrestamo(prestamo) && controlador.actualizarStockLibro(cod_libro, -1)) {
                JOptionPane.showMessageDialog(null, "Se registro el prestamo con codigo: " + id_prestamo);
                limpiarCampos();
            }
        } catch (PrestamoDuplicadoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            limpiarCampos();
        }        
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void limpiarCampos(){
        txtId_prestamo.setText("");
        jDateChooser1.setDate(null);
        jDateChooser2.setDate(null);
        cboLibros.setSelectedIndex(0);
    }
