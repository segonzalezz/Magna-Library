    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String nom_persona = txtNombreP.getText();
        int ed_persona = Integer.valueOf(txtEdadP.getText());
        String ced_persona = txtCedulaP.getText();
        String dir_persona = txtDireccionP.getText();
        String user_u = txtUserU.getText();
        String pass_u = txtPasswordU.getText();
        String selectedValue = jComboBoxR.getSelectedItem().toString();
        String[] partes = selectedValue.split(" - ");
        String cod_rol = partes[0];
        Usuario usuario = new Usuario(nom_persona, ed_persona, ced_persona, dir_persona, user_u, pass_u, cod_rol);
        try {
            if (controladorDos.registrarU(usuario)) {
                JOptionPane.showMessageDialog(null, "Se registr칩 el usuario correctamente con cedula: " + ced_persona);
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(null, "No se registr칩 el usuario con cedula: " + ced_persona);
            }
        } catch (UsuarioDuplicadoException e) {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String ced_persona = txtCedulaP.getText();
        Usuario usuario = controladorDos.obtenerU(ced_persona);
        if(usuario != null){
            txtNombreP.setText(usuario.getNom_persona());
            txtEdadP.setText(String.valueOf(usuario.getEd_persona()));
            txtDireccionP.setText(usuario.getDir_persona());
            txtUserU.setText(usuario.getUser_u());
            txtPasswordU.setText(usuario.getPass_u());
        }else{
            JOptionPane.showMessageDialog(null, "No se encontro ningun usuario con la cedula: " + ced_persona);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

