    private void txtEdadPKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEdadPKeyTyped
        int key = evt.getKeyChar();
        boolean numero = key >= 48 && key <= 57;
        if (!numero) {
            evt.consume();
        }
    }//GEN-LAST:event_txtEdadPKeyTyped

