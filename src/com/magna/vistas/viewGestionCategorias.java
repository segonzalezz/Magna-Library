    public viewGestionCategorias() {
        initComponents();
        setLocationRelativeTo(this);
        controlador = new ControladorRegistroCategoria();
        try{
            DefaultTableModel modelo = new DefaultTableModel();
            tableC.setModel(modelo);
            PreparedStatement ps = null;
            ResultSet rs = null;
            Singleton conexion = new Singleton();
            Connection con = (Connection) conexion.conectar();
            String selectQuery = "SELECT * FROM categoria";
            ps = con.prepareStatement(selectQuery);
            rs = ps.executeQuery();
            ResultSetMetaData rsMd = (ResultSetMetaData) rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            modelo.addColumn("CODIGO CATEGORIA");
            modelo.addColumn("NOMBRE CATEGORIA");
            int [] anchos = {100, 100};
            for (int i = 0; i < tableC.getColumnCount(); i++) {
                tableC.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }
            while(rs.next()){
                Object[] filas = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modelo.addRow(filas);
            }  
        } catch (SQLException ex){
            System.err.println(ex.toString());
        }
    }

