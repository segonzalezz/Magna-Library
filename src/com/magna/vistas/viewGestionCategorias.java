    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
      int filaSeleccionada = tableC.getSelectedRow();
        if (filaSeleccionada != -1) {
            String idEliminar = (String) tableC.getValueAt(filaSeleccionada, 0);
            if (controlador.eliminarCategoria(idEliminar)) {
                DefaultTableModel model = (DefaultTableModel) tableC.getModel();
                model.removeRow(filaSeleccionada);
                JOptionPane.showMessageDialog(null, "Categoria eliminada con codigo: " + idEliminar);
            } else {
                JOptionPane.showMessageDialog(null, "No se encontr√≥ categoria con codigo: " + idEliminar);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecciona una categoria para eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

